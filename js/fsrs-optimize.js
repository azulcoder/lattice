// FSRS weight optimisation from a user's own review history (Phase S5).
//
// Standard FSRS training: replay each card's ordered review sequence under
// candidate weights, predict the retrievability R at each review, and minimise
// the log-loss of R against whether the card was actually recalled
// (grade ≥ 2 = recalled; Again = lapse). We fit a tractable, well-conditioned
// SUBSET of the 17 weights by coordinate descent — initial stabilities
// w[0..3], the stability-growth/decay terms w[8..10], and the post-lapse
// scale w[11] — holding the rest at the published defaults. Pure functions,
// no storage/DOM, so the optimiser is fully unit-testable
// (scripts/verify-fsrs-optimize.mjs recovers a lower loss from synthetic data
// generated by known weights).

import {
  FSRS_DEFAULT_W, initStability, initDifficulty, nextDifficulty,
  stabilityOnSuccess, stabilityOnLapse, retrievability,
} from './fsrs.js';

const TUNABLE = [0, 1, 2, 3, 8, 9, 10, 11];
// Per-weight bounds (kept positive and in sane FSRS ranges).
const BOUNDS = {
  0: [0.05, 5], 1: [0.1, 8], 2: [0.5, 12], 3: [1, 30],
  8: [0.5, 4], 9: [0.02, 0.6], 10: [0.4, 2.5], 11: [0.5, 5],
};
const MIN_PREDICTIONS = 50;
const EPS = 1e-6;

// Group a flat review log into ordered per-card sequences. Each log entry:
// { cardId, ts (ISO), grade (1-4), elapsed (days since this card's prior review) }.
export function groupReviews(log) {
  const byCard = new Map();
  for (const r of log) {
    if (!r || !r.cardId || !(r.grade >= 1 && r.grade <= 4)) continue;
    if (!byCard.has(r.cardId)) byCard.set(r.cardId, []);
    byCard.get(r.cardId).push(r);
  }
  for (const arr of byCard.values()) arr.sort((a, b) => String(a.ts).localeCompare(String(b.ts)));
  return byCard;
}

// Log-loss contribution of one card's review sequence under weights w.
// The first review only initialises state (no prediction); each later review
// is a prediction of recall at its elapsed interval.
export function cardLogLoss(w, reviews) {
  if (reviews.length < 2) return { loss: 0, n: 0 };
  let S = initStability(reviews[0].grade, w);
  let D = initDifficulty(reviews[0].grade, w);
  let loss = 0, n = 0;
  for (let i = 1; i < reviews.length; i++) {
    const grade = reviews[i].grade;
    const elapsed = Math.max(0, reviews[i].elapsed || 0);
    const R = Math.min(1 - EPS, Math.max(EPS, retrievability(elapsed, S)));
    const recalled = grade >= 2 ? 1 : 0;
    loss += -(recalled * Math.log(R) + (1 - recalled) * Math.log(1 - R));
    n++;
    D = nextDifficulty(D, grade, w);
    S = Math.max(0.1, grade === 1 ? stabilityOnLapse(D, S, R, w) : stabilityOnSuccess(D, S, R, grade, w));
  }
  return { loss, n };
}

// Mean log-loss per prediction across all cards.
export function totalLogLoss(w, grouped) {
  let loss = 0, n = 0;
  for (const reviews of grouped.values()) {
    const c = cardLogLoss(w, reviews);
    loss += c.loss; n += c.n;
  }
  return n === 0 ? { loss: 0, n: 0, mean: 0 } : { loss, n, mean: loss / n };
}

// Coordinate-descent optimiser. Returns the (possibly unchanged) weights, the
// initial vs final mean log-loss, and whether optimisation actually ran.
export function optimizeWeights(log, opts = {}) {
  const iterations = opts.iterations || 25;
  const grouped = groupReviews(log);
  const initial = totalLogLoss(FSRS_DEFAULT_W, grouped);

  if (initial.n < (opts.minPredictions || MIN_PREDICTIONS)) {
    return { weights: FSRS_DEFAULT_W.slice(), mean: initial.mean, initialMean: initial.mean, predictions: initial.n, optimized: false };
  }

  const w = FSRS_DEFAULT_W.slice();
  let bestMean = initial.mean;
  // initial step per parameter = 20% of its range
  const step = {};
  for (const k of TUNABLE) step[k] = (BOUNDS[k][1] - BOUNDS[k][0]) * 0.2;

  for (let iter = 0; iter < iterations; iter++) {
    let improvedThisPass = false;
    for (const k of TUNABLE) {
      for (const dir of [+1, -1]) {
        const trial = w[k] + dir * step[k];
        const [lo, hi] = BOUNDS[k];
        if (trial < lo || trial > hi) continue;
        const saved = w[k];
        w[k] = trial;
        const m = totalLogLoss(w, grouped).mean;
        if (m < bestMean - 1e-9) { bestMean = m; improvedThisPass = true; }
        else { w[k] = saved; }
      }
    }
    if (!improvedThisPass) for (const k of TUNABLE) step[k] *= 0.5;   // refine when stuck
  }

  return { weights: w, mean: bestMean, initialMean: initial.mean, predictions: initial.n, optimized: true };
}
